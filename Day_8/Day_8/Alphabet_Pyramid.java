import java.util.*;
public class Alphabet_Pyramid {
    public static void main(String args[]){
        Scanner sc = new Scanner(System.in);
        int i, j;
        
        for(i =65; i<=69; i++){
            for(j=65; j<=i; j++){
                System.out.print((char)j + " ");
            }
        
        System.out.println();
        }
    }
}
